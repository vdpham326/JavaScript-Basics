let weather = 'dark';

switch (weather) {
    case 'rainy':
        console.log('Grab your umbrella');
        break;
    case 'sunny':
        console.log("It's a beautiful day!");
        break;
    case 'warm':
        console.log("It's a warm day.");
        break;
    default:
        console.log("It's a normal day.");
}